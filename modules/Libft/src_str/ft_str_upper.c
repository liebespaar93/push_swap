char	*ft_str_upper(char *str)
{
	char	*tmp;

	tmp = str;
	if (!str)
		return ((void *) 0);
	while (*tmp != '\0')
	{
		if (0x61 <= *tmp && *tmp <= 0x7a)
			*tmp -= 0x20;
		tmp++;
	}
	return (str);
}
